export const getBestSellers = () => products.filter((p) => p.badge === 'Best Seller' || p.rating >= 4.7).slice(0, 8);

export const getTrendingProducts = () => products.filter((p) => p.badge === 'Trending' || p.badge === 'New Arrival').slice(0, 4);

export const getProductsByCategory = (category: string) => 
  products.filter((p) => p.category.toLowerCase() === category.toLowerCase() || p.subcategory?.toLowerCase() === category.toLowerCase());

export const getProductById = (id: string) => products.find((p) => p.id === id);
